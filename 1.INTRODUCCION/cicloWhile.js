

/* 
Ciclo while
Es similar a for, pero con la diferencia de que solo tenemos un condicional.
Mientras se cumple el condicional=se ejecutara el ciclo.
*/

let contador=0;

while(contador<=10){
    
    console.log(contador);
    contador++;
}




